    /**
     * @param args the command line arguments
     */
    private void carregarTabela(ArrayList<TipoDeVeiculos> lista) {
        try {
            // Pega o modelo da tabela
            javax.swing.table.DefaultTableModel model
                    = (javax.swing.table.DefaultTableModel) jTable_Consulta_Tabela.getModel();

            model.setRowCount(0); // limpa a tabela

            // Preenche a tabela apenas com a lista filtrada
            for (TipoDeVeiculos veiculo : lista) {
                model.addRow(new Object[]{
                    veiculo.getIdVeiculo(),
                    veiculo.getPlaca(),
                    veiculo.getMarca(),
                    veiculo.getModelo(),
                    veiculo.getAnoDeFabricacao(),
                    veiculo.getStatus()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

    private void carregarTabelaOriginal() {
        try {
            // Pega o modelo da tabela
            javax.swing.table.DefaultTableModel model
                    = (javax.swing.table.DefaultTableModel) jTable_Consulta_Tabela.getModel();

            model.setRowCount(0); // limpa a tabela

            // Chama o DAO para pegar a lista de veículos
            
            
            ArrayList<TipoDeVeiculos> lista = cont.listar();

            // Preenche a tabela
            for (TipoDeVeiculos veiculo : lista) {
                model.addRow(new Object[]{
                    veiculo.getIdVeiculo(),
                    veiculo.getPlaca(),
                    veiculo.getMarca(),
                    veiculo.getModelo(),
                    veiculo.getAnoDeFabricacao(),
                    veiculo.getStatus()
                });
            }

        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }

